    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
       int row = table.getSelectedRow();
       btnSubmit.setEnabled(false);
        btnUpdate.setEnabled(true);
    id.setText(table.getValueAt(row, 1).toString());
    tournamentName.setText(table.getValueAt(row, 2).toString());

    try {
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");

        startDateChooser.setDate(sdf.parse(table.getValueAt(row, 3).toString()));
        endDateChooser.setDate(sdf.parse(table.getValueAt(row, 4).toString()));
    } catch (Exception e) {}

    cmbRounds.setSelectedItem(table.getValueAt(row, 5).toString());
    cmbType.setSelectedItem(table.getValueAt(row, 6).toString());

    // vimp
    String deptHead = table.getValueAt(row, 7).toString();

    for (int i = 0; i < cmbSportType.getItemCount(); i++) {
        Department d = cmbSportType.getItemAt(i);

        if (d.getHead().equals(deptHead)) {
            cmbSportType.setSelectedIndex(i);
            break;
        }
    }
    
    }//GEN-LAST:event_tableMouseClicked

