    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
         String text = search.getText().toLowerCase();

        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0);

        List<Object[]> list = new TournamentDAO().getAllTournaments();

        int count = 1;

        for (Object[] row : list) {

            if (row[1].toString().toLowerCase().contains(text) ||  // Name
                row[5].toString().toLowerCase().contains(text) ||  // Type
                row[6].toString().toLowerCase().contains(text)) {  // Dept

                model.addRow(new Object[]{
                    count++,
                    row[0],
                    row[1],
                    row[2],
                    row[3],
                    row[4],
                    row[5],
                    row[6]
                });
            }
        } // TODO add your handling code here:
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
      // ✅ Check if row is selected (ID filled)
    if (!id.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Use UPDATE instead of SUBMIT for existing record!");
        return;
    }

    // 🔹 GET VALUES
    String name = tournamentName.getText().trim();
    Object roundObj = cmbRounds.getSelectedItem();
    Object typeObj = cmbType.getSelectedItem();
    Department d = (Department) cmbSportType.getSelectedItem();

    String startDate = getDate(startDateChooser);
    String endDate = getDate(endDateChooser);

    // 🔴 VALIDATION
    if (name.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Enter Tournament Name");
        return;
    }

    if (startDate == null) {
        JOptionPane.showMessageDialog(this, "Select Start Date");
        return;
    }

    if (endDate == null) {
        JOptionPane.showMessageDialog(this, "Select End Date");
        return;
    }

    if (roundObj == null) {
        JOptionPane.showMessageDialog(this, "Select Number of Rounds");
        return;
    }

    if (typeObj == null || typeObj.toString().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Select Tournament Type");
        return;
    }

    if (d == null) {
        JOptionPane.showMessageDialog(this, "Select Department");
        return;
    }

    if (startDate.compareTo(endDate) > 0) {
        JOptionPane.showMessageDialog(this, "End Date must be after Start Date");
        return;
    }

    // 🔹 SAVE
    Tournament t = new Tournament(
        name,
        startDate,
        endDate,
        Integer.parseInt(roundObj.toString()),
        typeObj.toString(),
        d.getId()
    );

    new TournamentDAO().addTournament(t);

    JOptionPane.showMessageDialog(this, "Tournament Added Successfully");

    refreshTable();
    clearFields();

    }//GEN-LAST:event_btnSubmitActionPerformed

