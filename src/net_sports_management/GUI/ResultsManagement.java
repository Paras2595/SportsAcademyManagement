        private void loadData() {

    cmbTournament.removeAllItems();
    cmbWinner.removeAllItems();
    cmbLooser.removeAllItems();

    // Tournament
    for (model.Tournament t : new dao.TournamentDAO().getAllTournamentObjects()) {
        cmbTournament.addItem(t);
    }

    // Teams
    for (model.Team t : new dao.TeamDAO().getAllTeamObjects()) {
        cmbWinner.addItem(t);
        cmbLooser.addItem(t);
    }
}
    
    
    private void clearFields() {
        btnSubmit.setEnabled(true);
        btnUpdate.setEnabled(true);
         txtId.setText("");

    if (cmbTournament.getItemCount() > 0)
        cmbTournament.setSelectedIndex(0);

    if (cmbWinner.getItemCount() > 0)
        cmbWinner.setSelectedIndex(0);

    if (cmbLooser.getItemCount() > 0)
        cmbLooser.setSelectedIndex(0);
    }

    private void refreshTable() {
         DefaultTableModel model = (DefaultTableModel) table.getModel();
    model.setRowCount(0);

    List<Object[]> list = new dao.ResultDAO().getAllResults();

    int count = 1;

    for (Object[] row : list) {

        model.addRow(new Object[]{
            count++,
            row[0], // ID
            row[1], // Tournament
            row[2], // Winner
            row[3]  // Loser
        });
    }
    }
