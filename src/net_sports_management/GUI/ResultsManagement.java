    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
          model.Tournament tr = (model.Tournament) cmbTournament.getSelectedItem();
    model.Team winner = (model.Team) cmbWinner.getSelectedItem();
    model.Team loser = (model.Team) cmbLooser.getSelectedItem();

    // VALIDATION

    if (tr == null) {
        JOptionPane.showMessageDialog(this, "Select Tournament");
        return;
    }

    if (winner == null) {
        JOptionPane.showMessageDialog(this, "Select Winner Team");
        return;
    }

    if (loser == null) {
        JOptionPane.showMessageDialog(this, "Select Loser Team");
        return;
    }

    if (winner.getId() == loser.getId()) {
        JOptionPane.showMessageDialog(this, "Winner and Loser cannot be same");
        return;
    }

    // CREATE OBJECT
    model.Result r = new model.Result(
        tr.getId(),
        winner.getId(),
        loser.getId()
    );

    new dao.ResultDAO().addResult(r);

    JOptionPane.showMessageDialog(this, "Result Added Successfully");

    refreshTable();
    clearFields();
    }//GEN-LAST:event_btnSubmitActionPerformed

