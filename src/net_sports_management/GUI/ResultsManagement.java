    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
  int row = table.getSelectedRow();

    if (row == -1) return;

    txtId.setText(table.getValueAt(row, 1).toString());

    String tournamentName = table.getValueAt(row, 2).toString();
    String winnerName = table.getValueAt(row, 3).toString();
    String looserName = table.getValueAt(row, 4).toString();

    //  Tournament
    for (int i = 0; i < cmbTournament.getItemCount(); i++) {
        model.Tournament t = cmbTournament.getItemAt(i);
        if (t.getTournamentName().equals(tournamentName)) {
            cmbTournament.setSelectedIndex(i);
            break;
        }
    }

    //  Winner
    for (int i = 0; i < cmbWinner.getItemCount(); i++) {
        model.Team t = cmbWinner.getItemAt(i);
        if (t.getTeamName().equals(winnerName)) {
            cmbWinner.setSelectedIndex(i);
            break;
        }
    }

    //  LOSER 
    for (int i = 0; i < cmbLooser.getItemCount(); i++) {
        model.Team t = cmbLooser.getItemAt(i);
        if (t.getTeamName().equals(looserName)) {
            cmbLooser.setSelectedIndex(i);
            break;
        }
    }  
    }//GEN-LAST:event_btnDeleteActionPerformed

