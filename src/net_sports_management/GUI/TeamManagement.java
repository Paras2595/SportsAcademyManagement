    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        btnSubmit.setEnabled(false);
        btnUpdate.setEnabled(true);
        int row = table.getSelectedRow();

    if (row == -1) return;

    //  Set basic fields
    txtId.setText(table.getValueAt(row, 1).toString());
    teamName.setText(table.getValueAt(row, 2).toString());

    //  Department selection
    String deptName = table.getValueAt(row, 3).toString();

    for (int i = 0; i < cmbDepartment.getItemCount(); i++) {
        Department d = (Department) cmbDepartment.getItemAt(i);
        if (d.toString().equals(deptName)) {
            cmbDepartment.setSelectedIndex(i);
            break;
        }
    }

    //  Coach selection
    String coachName = table.getValueAt(row, 4).toString();

    for (int i = 0; i < cmbCoach.getItemCount(); i++) {
        Coach c = (Coach) cmbCoach.getItemAt(i);
        if (c.getName().equals(coachName)) {
            cmbCoach.setSelectedIndex(i);
            break;
        }
    }
    }//GEN-LAST:event_tableMouseClicked

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
                  String text = search.getText().toLowerCase();

    DefaultTableModel model = (DefaultTableModel) table.getModel();
    model.setRowCount(0);

    List<Object[]> list = new TeamDAO().getAllTeams(); //  correct

    int count = 1;

    for (Object[] row : list) {

        if (row[1].toString().toLowerCase().contains(text) ||
            row[2].toString().toLowerCase().contains(text) ||
            row[3].toString().toLowerCase().contains(text)) {

            model.addRow(new Object[]{
                count++,
                row[0], // ID
                row[1], // Name
                row[2], // Department
                row[3]  // Coach
            });
        }
    } 
    }//GEN-LAST:event_btnSearchActionPerformed

