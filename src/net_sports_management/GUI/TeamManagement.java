    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
                    String name = teamName.getText().trim();
    Department dept = (Department) cmbDepartment.getSelectedItem();
    Coach coach = (Coach) cmbCoach.getSelectedItem();
    
    

    // 🔴 VALIDATION
    if (name.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Enter Team Name");
        return;
    }

    if (dept == null) {
        JOptionPane.showMessageDialog(this, "Select Department");
        return;
    }

    if (coach == null) {
        JOptionPane.showMessageDialog(this, "Select Coach");
        return;
    }

    // 🔹 CREATE OBJECT
    Team t = new Team(name, dept.getId(), coach.getId());

    new TeamDAO().addTeam(t);

    JOptionPane.showMessageDialog(this, "Team Added Successfully");

    refreshTable();
    clearFields();  // TODO add your handling code here:
    }//GEN-LAST:event_btnSubmitActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
                   String idText = txtId.getText().trim();
    String name = teamName.getText().trim();
    Department dept = (Department) cmbDepartment.getSelectedItem();
    Coach coach = (Coach) cmbCoach.getSelectedItem();

    if (idText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Select a team to update!");
        return;
    }

    if (name.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Enter Team Name");
        return;
    }

    if (dept == null) {
        JOptionPane.showMessageDialog(this, "Select Department");
        return;
    }

    if (coach == null) {
        JOptionPane.showMessageDialog(this, "Select Coach");
        return;
    }

    int id = Integer.parseInt(idText);

    Team t = new Team(id, name, dept.getId(), coach.getId()); // ✅ include ID

    new TeamDAO().updateTeam(t); // ✅ correct method

    JOptionPane.showMessageDialog(this, "Team Updated Successfully");

    refreshTable();
    clearFields();
    }//GEN-LAST:event_btnUpdateActionPerformed

