    private void loadDepartments() {
     DepartmentDAO dao = new DepartmentDAO();
        List<Department> list = dao.getAllDepartments();

        cmbDepartment.removeAllItems();

        for (Department d : list) {
            cmbDepartment.addItem(d);
        }
}
  

    private void refreshTable() {
         DefaultTableModel model = (DefaultTableModel) table.getModel();
    model.setRowCount(0); // clear table

    TeamDAO dao = new TeamDAO();
    List<Object[]> list = dao.getAllTeams();  // from DAO

    int srNo = 1;

    for (Object[] row : list) {

        model.addRow(new Object[]{
            srNo++,        // Sr No
            row[0],        // Id
            row[1],        // Name
            row[2],        // DeptHead  
            row[3]         // Coach
        });
    }
    }

    private void clearFields() {
        btnSubmit.setEnabled(true);
        btnUpdate.setEnabled(true);
       
      txtId.setText("");
        teamName.setText("");
        if (cmbCoach.getItemCount() > 0) {
    cmbCoach.setSelectedIndex(0);
}
        if (cmbDepartment.getItemCount() > 0) {
            cmbDepartment.setSelectedIndex(0);
        }
    }

    private void loadCoaches() {
    CoachDAO dao = new CoachDAO();
    List<Coach> list = dao.getAllCoaches();

    cmbCoach.removeAllItems();

    for (Coach c : list) {
        cmbCoach.addItem(c);
    }    
    }

   
