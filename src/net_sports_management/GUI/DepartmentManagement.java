    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
       String idText = deptId.getText().trim();

    // 🚫 Prevent insert if row is selected
    if (!idText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Use UPDATE instead of SUBMIT for existing record!");
        return;
    }

    String name = deptName.getText().trim();
    String head = deptHead.getText().trim();

    if (name.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Department name is required!");
        return;
    }

    if (head.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Department head is required!");
        return;
    }

    Department d = new Department(name, head);
    new DepartmentDAO().addDepartment(d);

    JOptionPane.showMessageDialog(this, "Department Added Successfully ✅");

    refreshTable();
    clearFields();
    }//GEN-LAST:event_btnSubmitActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
       String idText = deptId.getText().trim();
    String name = deptName.getText().trim();
    String head = deptHead.getText().trim();

    if (idText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please select a department to update!");
        return;
    }

    if (name.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Department name is required!");
        return;
    }

    if (head.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Department head is required!");
        return;
    }

    int id = Integer.parseInt(idText);

    Department d = new Department(id, name, head); // include ID
    new DepartmentDAO().updateDepartment(d);       // ✅ correct method

    JOptionPane.showMessageDialog(this, "Department Updated Successfully ✅");

    refreshTable();
    clearFields();
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
       int row = table.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Select row first!");
            return;
        }

        int id = Integer.parseInt(table.getValueAt(row, 0).toString());
        new DepartmentDAO().deleteDepartment(id);

        refreshTable();
        clearFields();        
    }//GEN-LAST:event_btnDeleteActionPerformed

