    private void refreshTable() {
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0);

        List<Department> list = new DepartmentDAO().getAllDepartments();

        for (Department d : list) {
            model.addRow(new Object[]{
                    d.getId(),
                    d.getName(),
                    d.getHead()
            });
        }
    }

