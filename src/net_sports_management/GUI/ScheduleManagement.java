    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String text = search.getText().toLowerCase();

        DefaultTableModel model = (DefaultTableModel) table1.getModel();
        model.setRowCount(0);

        List<Object[]> list = new dao.ScheduleDAO().getAllSchedules();

        int count = 1;

        for (Object[] row : list) {

            if (row[1].toString().toLowerCase().contains(text) ||
                row[2].toString().toLowerCase().contains(text) ||
                row[3].toString().toLowerCase().contains(text)) {

                model.addRow(new Object[]{
                    count++,
                    row[0], // ID
                    row[1], // Team
                    row[2], // Tournament
                    row[3], // Department
                    row[4], // Location
                    row[5]  // Date
                });
            }
        } 
    }//GEN-LAST:event_btnSearchActionPerformed

