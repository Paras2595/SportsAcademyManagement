    private void table1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table1MouseClicked
            int row = table1.getSelectedRow();
                btnSubmit.setEnabled(false);
        btnUpdate.setEnabled(true);
    if (row == -1) return;

    // ✅ Safe getter
    Object idObj = table1.getValueAt(row, 1);
    Object teamObj = table1.getValueAt(row, 2);
    Object tournamentObj = table1.getValueAt(row, 3);
    Object deptObj = table1.getValueAt(row, 4);
    Object locationObj = table1.getValueAt(row, 5);
    Object dateObj = table1.getValueAt(row, 6);

    // 🔴 NULL CHECK
    if (idObj == null) {
        JOptionPane.showMessageDialog(this, "Invalid row selected!");
        return;
    }

    txtId.setText(idObj.toString());
    txtLocation.setText(locationObj != null ? locationObj.toString() : "");

    String teamName = (teamObj != null) ? teamObj.toString() : "";
    String tournamentName = (tournamentObj != null) ? tournamentObj.toString() : "";
    String deptName = (deptObj != null) ? deptObj.toString() : "";

    // ✅ Set ComboBoxes safely
    for (int i = 0; i < cmbTeam.getItemCount(); i++) {
        if (cmbTeam.getItemAt(i).toString().equals(teamName)) {
            cmbTeam.setSelectedIndex(i);
            break;
        }
    }

    for (int i = 0; i < cmbTournament.getItemCount(); i++) {
        if (cmbTournament.getItemAt(i).toString().equals(tournamentName)) {
            cmbTournament.setSelectedIndex(i);
            break;
        }
    }

    for (int i = 0; i < cmbDepartment.getItemCount(); i++) {
        if (cmbDepartment.getItemAt(i).toString().equals(deptName)) {
            cmbDepartment.setSelectedIndex(i);
            break;
        }
    }

    // ✅ Date safe parse
    if (dateObj != null) {
        try {
            java.util.Date d = new java.text.SimpleDateFormat("yyyy-MM-dd")
                    .parse(dateObj.toString());
            jDateChooser.setDate(d);
        } catch (Exception e) {
            jDateChooser.setDate(null);
        }
    } else {
        jDateChooser.setDate(null);
    }
    }//GEN-LAST:event_table1MouseClicked

