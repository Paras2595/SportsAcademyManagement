    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
         model.Team team = (model.Team) cmbTeam.getSelectedItem();
    model.Tournament t = (model.Tournament) cmbTournament.getSelectedItem();
    model.Department d = (model.Department) cmbDepartment.getSelectedItem();

    String location = txtLocation.getText().trim();
            if (!txtId.getText().trim().isEmpty()) {
    JOptionPane.showMessageDialog(this, "Use UPDATE instead of SUBMIT for existing record!");
    return;
}

    if (team == null) {
        JOptionPane.showMessageDialog(this, "Select Team");
        return;
    }

    if (t == null) {
        JOptionPane.showMessageDialog(this, "Select Tournament");
        return;
    }

    if (d == null) {
        JOptionPane.showMessageDialog(this, "Select Department");
        return;
    }

    if (location.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Enter Location");
        return;
    }

   if (jDateChooser.getDate() == null) {
    JOptionPane.showMessageDialog(this, "Select Date");
    return;
}

    // 🔹 CREATE OBJECT
   model.Schedule s = new model.Schedule(
    team.getId(),
    t.getId(),
    d.getId(),
    location,
    getDate()   
);

    // 🔹 SAVE
    new dao.ScheduleDAO().addSchedule(s);

    JOptionPane.showMessageDialog(this, "Schedule Added Successfully");

    refreshTable();
    clearFields();
    }//GEN-LAST:event_btnSubmitActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
                                           
         

    int id = Integer.parseInt(txtId.getText());

    model.Team team = (model.Team) cmbTeam.getSelectedItem();
    model.Tournament t = (model.Tournament) cmbTournament.getSelectedItem();
    model.Department d = (model.Department) cmbDepartment.getSelectedItem();

    if (team == null || t == null || d == null) {
        JOptionPane.showMessageDialog(this, "Select all fields!");
        return;
    }

    if (txtLocation.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Location is required!");
        return;
    }

    model.Schedule s = new model.Schedule(
        id,   // ✅ ID comes from selected row
        team.getId(),
        t.getId(),
        d.getId(),
        txtLocation.getText(),
        getDate()
    );

    new dao.ScheduleDAO().updateSchedule(s);

    JOptionPane.showMessageDialog(this, "Updated Successfully ✅");

    refreshTable();
    clearFields();
                              
    }//GEN-LAST:event_btnUpdateActionPerformed

