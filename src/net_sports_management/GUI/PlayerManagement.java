    private void refreshTable() {
        
     DefaultTableModel model = (DefaultTableModel) table.getModel();
    model.setRowCount(0); // clear table

    PlayerDAO dao = new PlayerDAO();
    List<Object[]> list = dao.getAllPlayers();

    for (int i = 0; i < list.size(); i++) {

    Object[] row = list.get(i);

    model.addRow(new Object[]{
        i+1,  // Sr No (auto)
        row[0],
        row[1],
        row[2],
        row[3]
    });
}
        
            }

    private void clearFields() {
        btnSubmit.setEnabled(true);
        btnUpdate.setEnabled(true);
         txtId.setText("");
    txtName.setText("");

    if (cmbDepartment.getItemCount() > 0) {
        cmbDepartment.setSelectedIndex(0);
    }

    if (cmbTeam.getItemCount() > 0) {
        cmbTeam.setSelectedIndex(0);
    }
    }

    private void loadDepartments() {
        DepartmentDAO dao = new DepartmentDAO();
        List<Department> list = dao.getAllDepartments();

        cmbDepartment.removeAllItems();

        for (Department d : list) {
            cmbDepartment.addItem(d);
        }    }

    private void loadTeams() {
      TeamDAO dao = new TeamDAO();
    List<Object[]> list = dao.getAllTeams();

    cmbTeam.removeAllItems();

    for (Object[] row : list) {

        Team t = new Team();

        t.setId(Integer.parseInt(row[0].toString())); // Id
        t.setName(row[1].toString());                 // Name

        cmbTeam.addItem(t); //  add object
    }
    }
