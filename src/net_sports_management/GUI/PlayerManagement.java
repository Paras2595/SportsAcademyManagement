    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
          String text = search.getText().toLowerCase();

    DefaultTableModel model = (DefaultTableModel) table.getModel();
    model.setRowCount(0);

    List<Object[]> list = new PlayerDAO().getAllPlayers();

    int count = 1;

    for (Object[] row : list) {

        if (row[1].toString().toLowerCase().contains(text) ||  // Name
            row[2].toString().toLowerCase().contains(text) ||  // Dept
            row[3].toString().toLowerCase().contains(text)) {  // Team

            model.addRow(new Object[]{
                count++,
                row[0], // ID
                row[1], // Name
                row[2], // Dept
                row[3]  // Team
            });
        }
    }  
    }//GEN-LAST:event_btnSearchActionPerformed

