    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
          String name = txtName.getText().trim();
          
    
    // ✅ Validation
    if (name.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Player name is required");
        return;
    }

    if (cmbDepartment.getSelectedItem() == null) {
        JOptionPane.showMessageDialog(this, "Select Department");
        return;
    }

    if (cmbTeam.getSelectedItem() == null) {
        JOptionPane.showMessageDialog(this, "Select Team");
        return;
    }

    Department dept = (Department) cmbDepartment.getSelectedItem();
    Team team = (Team) cmbTeam.getSelectedItem();

    Player p = new Player(name, dept.getId(), team.getId());

    new PlayerDAO().addPlayer(p);

    JOptionPane.showMessageDialog(this, "Player Added Successfully ✅");

    refreshTable();
    clearFields();  
      
    }//GEN-LAST:event_btnSubmitActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
      int row = table.getSelectedRow();

    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Select row first");
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(
        this,
        "Are you sure to delete?",
        "Confirm",
        JOptionPane.YES_NO_OPTION
    );

    if (confirm != JOptionPane.YES_OPTION) return;

    int id = Integer.parseInt(table.getValueAt(row, 1).toString());

    new dao.ResultDAO().deleteResult(id);

    JOptionPane.showMessageDialog(this, "Deleted Successfully");

    refreshTable();
    clearFields();
    }//GEN-LAST:event_btnUpdateActionPerformed

