    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
    int row = table.getSelectedRow();
    if (row == -1) return;
                btnSubmit.setEnabled(false);
        btnUpdate.setEnabled(true);
    txtId.setText(table.getValueAt(row, 1).toString());
    txtName.setText(table.getValueAt(row, 2).toString());

    String deptName = table.getValueAt(row, 3).toString();
    String teamName = table.getValueAt(row, 4).toString();

    // Select Department
    for (int i = 0; i < cmbDepartment.getItemCount(); i++) {
        Department d = (Department) cmbDepartment.getItemAt(i);
        if (d.toString().equals(deptName)) {
            cmbDepartment.setSelectedIndex(i);
            break;
        }
    }

    // Select Team
    for (int i = 0; i < cmbTeam.getItemCount(); i++) {
        Team t = (Team) cmbTeam.getItemAt(i);
        if (t.toString().equals(teamName)) {
            cmbTeam.setSelectedIndex(i);
            break;
        }
    }
        // TODO add your handling code here:
    }//GEN-LAST:event_tableMouseClicked

